# OPERADORES DE COMPARACION

x = 5
y = 3
z = 5

print(x == y) # ¿Sí es igual? True
print(x != y) # Si x no es igual True
print(x > y) # Si x es mayor True
print(x < y) # si x es menor True
print(x >= z) # Si x es mayor o igual True
print(x <= y) # Si x es menor o igual True


# UTILIZAR MÁS DE UN OPERADOR LÓGICO (AND, OR)
x > y and y > z # Si ambas son verdaderas = True
x > y or y > z # Si al menos una es verdadera = True


